a = int(input("첫번째 정수 입력>>"))
b = int(input("두번째 정수 입력>>"))


# 구조화되지 않은 코드
print("a + b = ", (a + b))
print("a - b = ", (a - b))
print("a * b = ", (a * b))
print("a / b = ", (a / b))


# 구조적인 프로그래밍
result = a + b
result1 = a - b
result2 = a * b
result3 = a / b

print("a + b = ", result)
print("a - b = ", result1)
print("a * b = ", result2)
print("a / b = ", result3)
